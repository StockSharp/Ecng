namespace Ecng.Common;

/// <summary>
/// Represents the phase of the moon.
/// </summary>
public enum LunarPhases
{
	/// <summary>
	/// The new moon phase.
	/// </summary>
	NewMoon,

	/// <summary>
	/// The waxing crescent phase.
	/// </summary>
	WaxingCrescent,

	/// <summary>
	/// The first quarter phase.
	/// </summary>
	FirstQuarter,

	/// <summary>
	/// The waxing gibbous phase.
	/// </summary>
	WaxingGibbous,

	/// <summary>
	/// The full moon phase.
	/// </summary>
	FullMoon,

	/// <summary>
	/// The waning gibbous phase.
	/// </summary>
	WaningGibbous,

	/// <summary>
	/// The last quarter phase.
	/// </summary>
	LastQuarter,

	/// <summary>
	/// The waning crescent phase.
	/// </summary>
	WaningCrescent,
}