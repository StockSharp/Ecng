namespace Ecng.Compilation;

/// <summary>
/// Represents the type of a compilation error.
/// </summary>
public enum CompilationErrorTypes
{
	/// <summary>
	/// Represents an informational message.
	/// </summary>
	Info,

	/// <summary>
	/// Represents a warning message.
	/// </summary>
	Warning,

	/// <summary>
	/// Represents an error message.
	/// </summary>
	Error,
}